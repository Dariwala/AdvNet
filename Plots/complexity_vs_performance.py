import argparse
import matplotlib.pyplot as plt

if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument('--ref', type=str, default="bbr")

    args = parser.parse_args()

    fig, axs = plt.subplots(2, 3, figsize=(12, 8))

    for i, args.ref in enumerate(["bbr", "cubic", "reno", "vegas", "westwood"]):    
        p_scores_old = [sum([initial_p_scores[args.ref][3*i], initial_p_scores[args.ref][3*i+1],initial_p_scores[args.ref][3*i+2]]) / 3 for i in range(len(initial_p_scores[args.ref]) // 3)]
        p_scores_new = [sum([final_p_scores[args.ref][3*i], final_p_scores[args.ref][3*i+1],final_p_scores[args.ref][3*i+2]]) / 3 for i in range(len(final_p_scores[args.ref]) // 3)]
        c_scores_old = [sum([initial_c_scores[args.ref][3*i], initial_c_scores[args.ref][3*i+1],initial_c_scores[args.ref][3*i+2]]) / 3 for i in range(len(initial_c_scores[args.ref]) // 3)]
        c_scores_new = [sum([final_c_scores[args.ref][3*i], final_c_scores[args.ref][3*i+1],final_c_scores[args.ref][3*i+2]]) / 3 for i in range(len(final_c_scores[args.ref]) // 3)]
        timesteps = [2, 3, 4, 5]

        axs[i // 3, i % 3].plot(timesteps, p_scores_old, label="Initial p_scores")
        axs[i // 3, i % 3].plot(timesteps, p_scores_new, label="Final p_scores")
        axs[i // 3, i % 3].plot(timesteps, c_scores_old, label="Initial c_scores")
        axs[i // 3, i % 3].plot(timesteps, c_scores_new, label="Final c_scores")

        axs[i // 3, i % 3].set_title(args.ref)
        axs[i // 3, i % 3].set_ylabel("p_score/c_score")
        axs[i // 3, i % 3].set_xlabel("Number of timesteps")
        axs[i // 3, i % 3].set_xticks([2, 3, 4, 5])
    fig.delaxes(axs[1, 2])
    axs = axs.flatten()  # Flatten the 2D array of subplots for easy iteration
    # Create a single legend for the entire figure
    handles, labels = axs[0].get_legend_handles_labels()  # Get handles and labels from one of the subplots
    fig.legend(handles, labels, loc='center', ncol=1, bbox_to_anchor=(0.75, 0.4))  # Add the legend to the figure

    plt.tight_layout()
    plt.savefig('Plots/p_scores_and_c_scores_simplification.pdf', format='pdf')